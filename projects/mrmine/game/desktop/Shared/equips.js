function getTotalDps() {
    var totalDps = 0;
    for (var i = 0; i < battleInventory.length; i++) {
        if (battleInventory[i].length > 0) {
            var item = new WeaponCraftingItem(battleInventory[i][0]);
            var itemDps = item.getDps();
            totalDps += itemDps;
        }
    }
    return totalDps;
}

