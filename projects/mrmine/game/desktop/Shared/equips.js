function getInventoryWeaponSpeed(index) {
    var weaponId = battleInventory[index][0];
    var weaponLevel = battleInventory[index][4];
    return weaponStats[weaponId][weaponLevel].speed * STAT.weaponSpeedMultiplier();
}

function getInventoryWeaponAttack(index) {
    var weaponId = battleInventory[index][0];
    var weaponLevel = battleInventory[index][4];
    if (weaponLevel > weaponStats[weaponId].maxLevel) {
        weaponLevel = weaponStats[weaponId].maxLevel;
    }
    return weaponStats[weaponId][weaponLevel].attack;
}

function isMaxLevel(index) {
    var weaponId = battleInventory[index][0];
    var weaponLevel = battleInventory[index][4];
    return weaponLevel >= weaponStats[weaponId].maxLevel;
}

function getIncreaseInAttackForNextLevel(index) {
    if (isMaxLevel(index)) return 0;
    var weaponId = battleInventory[index][0];
    var weaponLevel = battleInventory[index][4];
    return weaponStats[weaponId][weaponLevel + 1].attack - weaponStats[weaponId][weaponLevel].attack;
}

function getIncreaseInSpeedForNextLevel(index) {
    if (isMaxLevel(index)) return 0;
    var weaponId = battleInventory[index][0];
    var weaponLevel = battleInventory[index][4];
    return weaponStats[weaponId][weaponLevel + 1].speed - weaponStats[weaponId][weaponLevel].speed;
}

function getInventoryWeaponIcon(index) {
    var weaponId = battleInventory[index][0];
    return weaponStats[weaponId].icon;
}

function getInventoryWeaponName(index) {
    var weaponId = battleInventory[index][0];
    return weaponStats[weaponId].translatedName;
}

function getInventoryWeaponActionText(index) {
    var weaponId = battleInventory[index][0];
    return weaponStats[weaponId].action;
}

function getInventoryWeaponIndexById(weaponId) {
    for (var i in battleInventory) {
        if (battleInventory[i][0] == weaponId) {
            return i;
        }
    }
    return -1;
}

